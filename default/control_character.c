#include <stdio.h>

int main() {
	printf("\"hello\"");
	return 0;
} // 큰따움표

int main2() {
	printf("Hello!\ngg!");
	return 0;
} // 줄바꿈

int main3() {
	printf("\tHello!");
	return 0;
} // 탭

int main() {
	printf("\'Hello!\'");
	return 0;
} // 작은 따움표

int main() {
	printf("\\Hello!\\");
	return 0;
} // 역슬레시
